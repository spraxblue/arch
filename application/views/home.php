<script>
  AOS.init({
    once: true
  });
  $(document).ready(function() {
    setTimeout(function() {
      $('.bar-logo-container').addClass('active')
    }, 300);
    setTimeout(function() {
      $('.logo-text-d').addClass('active')
    }, 500);
    $(document).on('click', '.nav-link', function() {
      var link = $(this).attr('data-link');
      $('section').css("display", "none");
      $('.' + link).css("display", "block");
      
      $('.main').removeClass('load');
      if (link == 'home') {
        $('.proj-container').removeClass('opened');
        $('main.main').removeClass('collapsed-open');
      } else {
        $('main.main').addClass('collapsed-open');
        if($('.proj-container')){
        $('.proj-container').addClass('opened');
        }
      }
    });
    $(document).on('click', '.proj-container', function() {
      $(this).toggleClass('opened')
      $('main.main').removeClass('load');
      $('.my-team-VbDJk .proj-container ').removeClass('opened');
      if ($('.proj-container').hasClass('opened')) {
        $('main.main').addClass('collapsed-open');
      } else {
        $('main.main').removeClass('collapsed-open');
      }
    });
    const slider = document.querySelector('.img-items');
    let isDown = false;
    let startX;
    let scrollLeft;

    slider.addEventListener('mousedown', (e) => {
      isDown = true;
      slider.classList.add('active');
      startX = e.pageX - slider.offsetLeft;
      scrollLeft = slider.scrollLeft;
    });
    slider.addEventListener('mouseleave', () => {
      isDown = false;
      slider.classList.remove('active');
    });
    slider.addEventListener('mouseup', () => {
      isDown = false;
      slider.classList.remove('active');
    });
    slider.addEventListener('mousemove', (e) => {
      if (!isDown) return;
      e.preventDefault();
      const x = e.pageX - slider.offsetLeft;
      const walk = (x - startX) * 3; //scroll-fast
      slider.scrollLeft = scrollLeft - walk;
    });

  });
  window.addEventListener("resize", function() {
    $('.main').removeClass('load');
  });
  setTimeout(() => {
    var lazyloadImages;
    lazyloadImages = document.querySelectorAll(".lazy");
    lazyloadImages.forEach(function(img) {
      img.src = img.dataset.src;
      img.classList.remove('lazy');
    });
  }, 1600);
  // Scroll to specific values
  // scrollTo is the same
  window.scroll({
    top: 2500,
    left: 0,
    behavior: 'smooth'
  });

  // Scroll certain amounts from current position 
  window.scrollBy({
    top: 100, // could be negative value
    left: 0,
    behavior: 'smooth'
  });
</script>